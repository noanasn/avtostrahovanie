                    <h2>Как правильно и безопасно перевозить ребенка в автомобиле.</h2>
                    <img src="/news/img_for_news/news3.jpg" alt="" style=" width: 50%;">
                    <p>Согласно данным российской Госавтоинспекции, в 2023 году случилось более 15 тыс. ДТП с участием детей. При этом 81% аварий произошел по причине нарушения ПДД водителями. Более половины (64%) всех жертв дорожно-транспортных происшествий в возрасте до 16 лет — это дети, которые были пассажирами.</p>
                    <p>Исходя из исследований ВОЗ, автокресла снижают смертность детей-пассажиров на 54%, риск получения травм — на 76%, а особо тяжелых травм — на 92%.</p>
                    <p>Эти цифры доказывают, что детские удерживающие устройства (ДУУ) стали неотъемлемой частью оснащения автомобиля и обязательным условием для начала любой поездки с ребенком.</p>
                    <h4>Типы детских удерживающих устройств</h4>
                    <p>ДУУ делятся на несколько типов: детское автокресло, бустер, спинка сиденья, ремень безопасности, детское сиденье для мотоцикла или велосипеда.</p>
                    <p>В России технические характеристики к детским удерживающим устройствам прописаны в ГОСТах:</p>
                    <ul>
                        <li>крепление: ремни безопасности автомобиля, система Isofix или Latch, крепление к раме велосипеда или мотоцикла;</li>
                        <li>материал каркаса: пластик, металл, композитные материалы;</li>
                        <li>материал обивки: текстиль, экокожа, искусственная кожа;</li>
                        <li>вес удерживающего устройства;</li>
                        <li>размеры устройства: ширина, глубина, высота;</li>
                        <li>угол наклона спинки: возможность регулировки положения ребенка в соответствии с его возрастом и комфортом;</li>
                        <li>система вентиляции: наличие отверстий или дополнительных материалов для обеспечения циркуляции воздуха внутри устройства;</li>
                        <li>наличие и тип боковой защиты: для поглощения энергии удара при боковом столкновении;
                            возможность снятия обивки для стирки или чистки.</li>
                    </ul>
