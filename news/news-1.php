                    <p>ОСАГО — один из самых распространенных и востребованных страховых продуктов. Из-за этого клиенты страховых компаний, оформляющие ОСАГО, часто становятся жертвами мошенников. Понять, что полис ОСАГО поддельный, можно по бланку страховки. Разобрались, как должен выглядеть бланк ОСАГО, что должно быть указано в полисе и как проверить его на подлинность.</p>
                    <h4>Какие данные должны быть указаны в бланке ОСАГО</h4>
                    <p>Стандартно в бланке ОСАГО указываются данные об автомобиле (марка, модель, СТС, номер VIN, государственный знак), собственнике и водителях (с номером прав и КБМ), допущенных к управлению авто. Также в полисе указывается срок его действия, размер страховой премии, цель использования авто (личное, учебная езда, такси и т. п.) и все коэффициенты ОСАГО. Наконец, в углу бланка будет QR-код, по которому можно проверить ОСАГО на подлинность по базе РСА.</p>

                    <p>Если вы оформляете ОСАГО через калькулятор на Банки.ру, вам достаточно ввести госномер своего автомобиля. После введения госномера система загрузит данные об авто. В 70% случаев все данные заполнятся автоматически. Но иногда часть информации об авто нужно вводить вручную. Также для авторизованных на сайте пользователей доступна возможность продолжить прошлый расчет, нажав на кнопку «Мои расчеты»: все данные подтянутся автоматически, повторно заполнять анкету не потребуется.</p>

                    <p>После введения всех данных на шаге «Автомобиль» вы перейдете к форме с вводом информации о водителе. Если их несколько, можно добавить до десяти человек, нажав кнопку «Добавить водителя». Если вы планируете купить полис «Мультидрайв» без ограничения на количество водителей, поставьте галочку в пункте «Без ограничений». Тогда в бланке ОСАГО не будут указаны водители, но будет стоять пометка об опции «Мультидрайв».</p>

                    <p>Заполняя данные водителей, необходимо указать данные водительского удостоверения. Если вы меняли права в течение года, введите также прошлое ВУ, чтобы применить скидку за безопасное вождение (КБМ).</p>

                    <p>Также при покупке полиса вы указываете свои паспортные данные (серию и номер паспорта, адрес регистрации и контактную информацию). Это нужно для договора со страховой компанией, в бланке ОСАГО эти данные не указываются.</p>

                    <h4>Как выглядит бланк ОСАГО</h4>
                    <p>Внешний вид бланка ОСАГО зависит от того, как вы его покупаете. Если вы оформляете ОСАГО в офисе страховой компании, вы получаете розовый бланк формата чуть больше листа А4.</p>

                    <p>Настоящий бланк страхового полиса должен содержать:</p>
                    <ul>
                        <li>водяной знак РСА, который видно на просвет;</li>
                        <li>выпуклые цифры серии и номера документа;</li>
                        <li>металлизированную нить;</li>
                        <li>печать страховой компании.</li>
                    </ul>

                    <p> Нестандартности могут быть признаком подделки.</p>
                    <p>ОСАГО в электронном виде выглядит так же (кроме цвета листа, нити и водяного знака). На нем также обязательно должен быть QR-код. Распечатывать приобретенный в электронном виде бланк ОСАГО не обязательно. Но если вы его распечатали, убедитесь, что QR-код не смазался: по нему сотрудник ГИБДД сможет проверить ваш полис</p>
